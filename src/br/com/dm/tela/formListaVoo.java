    public final void preencherCombo(){
    
        try {
            ArrayList<Localidade> listaLocalidade = new LocalidadeDAO().buscarTodos();
            
            DefaultComboBoxModel dcb = (DefaultComboBoxModel)PaisOrigem.getModel();
            DefaultComboBoxModel dcb2 = (DefaultComboBoxModel)PaisDes.getModel();
            
            dcb.removeAllElements();
            dcb2.removeAllElements();
            
            for(Localidade loc: listaLocalidade){
                dcb.addElement(loc.getPais());
                dcb2.addElement(loc.getPais());
                
            }
         } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro N√£o foi poss√≠vel localizar o DRIVER");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Problemas na conex√£o com o banco de dados");
        }
        
    
    }
