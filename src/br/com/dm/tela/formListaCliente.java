    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int idx = TabelaCli.getSelectedRow();
       
        
       if (idx < 0){
           JOptionPane.showMessageDialog(null, "Selecione um Passageiro");
       }
         else{
           
            String  nome = (String) TabelaCli.getValueAt(idx, 1);
            int id = (int)TabelaCli.getValueAt(idx, 0);
            
        if(JOptionPane.showConfirmDialog(null, "Deseja apagar o" + 
                "Cliente" +nome+ "?") == JOptionPane.YES_OPTION){
           
            try {
                
                Cliente c = new Cliente();
                c.setIdCliente(id);
                
                new ClienteDAO().apagar(c);
                
                preencherGrid(new ClienteDAO().buscarTodos());
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(formListaCliente.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(formListaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
         }
      }
    }//GEN-LAST:event_jButton2ActionPerformed

