    public void preencherGrid(ArrayList<Cliente> listaCliente) {
        String[] columnNames = {"nome", "cpf", "IdCliente"};
        DefaultTableModel dtm = new DefaultTableModel(columnNames , 0);
        
        for (Cliente c: listaCliente)
        {
            Object[] values = {c.getNome(),c.getCpf(),c.getIdCliente()};
            dtm.addRow(values);
        }
        getTabelaCli().setModel(dtm);
    }
    
